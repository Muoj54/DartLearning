void main() {
  // An Example of the int data type
  int age = 25;
  print('Age: $age');

  // An example of the double data type
  double height = 5.9;
  print('Height: $height feet');

  //An example of the String data type
  String name = 'John';
  print('Name: $name');

  // An example of the List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // An example of the Map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'height': 5.6,
    'fruits': ['Grapes', 'Kiwi']
  };
  print('Person Info: $person');

  
}